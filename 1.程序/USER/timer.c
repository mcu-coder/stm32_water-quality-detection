void TIM2_Init(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;
	NVIC_InitTypeDef NVIC_InitStructure;
	TIM_TimeBaseInitTypeDef  TIM_TimeBaseStructure;

	RCC_APB1PeriphClockCmd(RCC_APB1Periph_TIM2, ENABLE);	//使能TIM2时钟
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOA, ENABLE);  //使能GPIO外设时钟使能
	
	
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_0; //TIM2_CH1，PA0口
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IPD; //下拉
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;//运行频率50兆Hz
	GPIO_Init(GPIOA, &GPIO_InitStructure);

	TIM_DeInit(TIM2);//将外设TIM2寄存器重设为缺省值,
	
	TIM_TimeBaseStructure.TIM_Period = 100;//0xFFFF;//自动装载值65535
	
	TIM_TimeBaseStructure.TIM_Prescaler = 0x00;
	TIM_TimeBaseStructure.TIM_ClockDivision = 0x0;
	
	TIM_TimeBaseStructure.TIM_CounterMode = TIM_CounterMode_Up;//向上计数
	
	TIM_TimeBaseInit(TIM2, &TIM_TimeBaseStructure); // Time base configuration

	TIM_ETRClockMode1Config(TIM2, TIM_ExtTRGPSC_OFF, TIM_ExtTRGPolarity_NonInverted, 0);
	
	TIM_SetCounter(TIM2, 0);//把计数器缓存清零
	NVIC_InitStructure.NVIC_IRQChannel = TIM2_IRQn;  //TIM2中断
	NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority = 1;  //先占优先级1级
	NVIC_InitStructure.NVIC_IRQChannelSubPriority = 2;  //从优先级3级
	NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE; //IRQ通道被使能
	NVIC_Init(&NVIC_InitStructure);  //根据NVIC_InitStruct中指定的参数初始化外设NVIC寄存器
	
	TIM_ITConfig(TIM2,TIM_IT_Update,ENABLE);//允许更新中断
}

